public class Void {

    // Method with return type void
    public static void printMessage() {
        System.out.println("Hello, World!");
    }

    public static void main(String[] args) {
        printMessage();
    }
    
    /* 
     * Note 🧑🏻‍💻
     * The void keyword allows us to create methods which do not return a value. 
    */
}
